//Write a program  to print a table of 2
package com.practice;

public class WhileDemo1 {

	public static void main(String[] args) {
	
         int tableNo = 2;
         int i=1;
         while(i<=10) {
        	 System.out.print(i*tableNo + "  ");
        	 i++;
         }
         System.out.println();
	}

}
